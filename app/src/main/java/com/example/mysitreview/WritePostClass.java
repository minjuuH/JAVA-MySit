package com.example.mysitreview;

public class WritePostClass {
    private String title;
    private String introduce;
    private String starttime;
    private String endtime;
    private String content;
    private String type;
    private String now_popul;
    private String name;
    private String uid;
    private String max_popul;

    public WritePostClass(){;}
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIntroduce() {
        return introduce;
    }

    public void setIntroduce(String introduce) {
        this.introduce = introduce;
    }

    public String getStarttime() {
        return starttime;
    }

    public void setStarttime(String starttime) {
        this.starttime = starttime;
    }

    public String getEndtime() {
        return endtime;
    }

    public void setEndtime(String endtime) {
        this.endtime = endtime;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }


    public String getNow_popul() {
        return now_popul;
    }

    public void setNow_popul(String now_popul) {
        this.now_popul = now_popul;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }


    public String getMax_popul() {
        return max_popul;
    }

    public void setMax_popul(String max_popul) {
        this.max_popul = max_popul;
    }


}
